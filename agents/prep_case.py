#!/usr/bin/env python3
"""prep_case.py — предложный падеж топонимов для заголовков сводок.

ЗАЧЕМ. Заголовок строился как «Удар по НПЗ в {city}», где city — именительный:
«в Саратов», «в Керчь», «в Сызрань», «в Ленинградская область». Это 62 SEO-title
в выдаче Яндекса.

ПОЧЕМУ НЕ pymorphy. Ради одной строки тащить на VPS словарь на 30 МБ не стоит.
Список топонимов в архиве конечен (102 на 16.07), правило проверено на каждом:
верно ~90, остальные — в EXCEPTIONS. Незнакомый город правило обработает по общему
случаю (Саратов→Саратове), а промах видно в заголовке сразу.

Проверка: python3 agents/prep_case.py --selfcheck
Полный прогон по архиву: python3 agents/prep_case.py data/strikes.json
"""
import re
import sys

# Там, где правило врёт или топоним несклоняемый. Проверено на всех 102 городах архива.
EXCEPTIONS = {
    # особый предложный
    "Крым": "Крыму",
    "Крым (множественные объекты)": "Крыму (множественные объекты)",
    # дефисные: склоняется только голова
    "Ростов-на-Дону": "Ростове-на-Дону",
    "Славянск-на-Кубани": "Славянске-на-Кубани",
    # несклоняемые
    "Тольятти": "Тольятти",
    "Сочи": "Сочи",
    "НПС Черкассы": "НПС Черкассы",
    # мн.ч., которые правило не ловит (короткие)
    "Саки": "Саках",
    "Ички (Советский)": "Ичках (Советский)",
    # многословные
    "Старая Нелидовка": "Старой Нелидовке",
    "порт Кавказ": "порту Кавказ",
    "Крымский район": "Крымском районе",
    "Ленинградская область": "Ленинградской области",
    # акватории
    "Чёрное море (акватория)": "Чёрном море",
    "Азовское море (акватория)": "Азовском море",
    "Азовское море": "Азовском море",
    "Азовское море (Таганрогский залив)": "Азовском море (Таганрогский залив)",
    "Балтийское море (акватория)": "Балтийском море",
    "Каспийское море (акватория)": "Каспийском море",
    "Чёрное море (район КТК)": "Чёрном море (район КТК)",
    "Керченский пролив": "Керченском проливе",
    "Таганрогский залив": "Таганрогском заливе",
}

PREFIXES = ("пгт ", "х. ", "с. ", "г. ", "пос. ", "ст. ", "д. ")
# м.р. на -ь: Ставрополь→Ставрополе, Ярославль→Ярославле. Ж.р. даёт -и: Керчь→Керчи.
MASC_SOFT = ("поль", "вль", "мль", "боль")


def _word(c):
    """Одно слово в предложный. Аббревиатуры и латиница — как есть."""
    if not c or c.isupper() or re.search(r"[A-Za-z]", c):
        return c
    low = c.lower()

    # прилагательные: Ильский→Ильском, Гвардейское→Гвардейском, Полтавская→Полтавской
    if low.endswith(("ский", "цкий", "ный", "жный", "ый", "ий", "ой")):
        return c[:-2] + "ом"
    if low.endswith(("ское", "цкое", "ное", "ое")):
        return c[:-2] + "ом"
    if low.endswith(("ская", "цкая", "ная", "ая")):
        return c[:-2] + "ой"

    # мн.ч.: Вязники→Вязниках, Чебоксары→Чебоксарах, Люберцы→Люберцах
    if low.endswith(("ки", "ны", "цы", "ры", "ги", "ви", "ли", "ти", "ди", "ни")) and len(c) >= 4:
        return c[:-1] + "ах"

    if low.endswith("ия"):
        return c[:-1] + "и"
    if low.endswith(("а", "я")):
        return c[:-1] + "е"
    if low.endswith("о"):
        return c[:-1] + "е"
    if low.endswith("ь"):
        return c[:-1] + ("е" if low.endswith(MASC_SOFT) else "и")
    if low.endswith("й"):
        return c[:-1] + "е"
    if re.search(r"[бвгджзклмнпрстфхцчшщ]$", low):
        return c + "е"
    return c


def prep(city):
    """Топоним в предложном («в <prep(city)>»). Не уверены — возвращаем как есть."""
    c = (city or "").strip()
    if not c:
        return c
    if c in EXCEPTIONS:
        return EXCEPTIONS[c]

    for p in PREFIXES:
        if c.startswith(p):
            return p + prep(c[len(p):])

    # «Советский (Крым)» — склоняем голову, хвост в скобках не трогаем
    m = re.match(r"^([^(]+?)\s*(\([^)]*\))$", c)
    if m:
        return "%s %s" % (prep(m.group(1)), m.group(2))

    # многословные: «Крымский район» → «Крымском районе» (склоняем каждое слово)
    if " " in c and "-" not in c:
        return " ".join(_word(w) for w in c.split())

    return _word(c)


def selfcheck():
    cases = {
        "Саратов": "Саратове",          # согласный — общий случай
        "Керчь": "Керчи",               # ж.р. на -ь
        "Ставрополь": "Ставрополе",     # м.р. на -ь
        "Сызрань": "Сызрани",
        "Москва": "Москве",
        "Кстово": "Кстове",
        "Ильский": "Ильском",           # прилагательное
        "Гвардейское": "Гвардейском",
        "Чебоксары": "Чебоксарах",      # мн.ч.
        "Крым": "Крыму",                # особый предложный
        "Ростов-на-Дону": "Ростове-на-Дону",
        "Тольятти": "Тольятти",         # несклоняемое
        "Ленинградская область": "Ленинградской области",
        "Крымский район": "Крымском районе",
        "пгт Афипский": "пгт Афипском",
        "х. Вязники": "х. Вязниках",
        "Советский (Крым)": "Советском (Крым)",
        "Чёрное море (акватория)": "Чёрном море",
        "НПС Черкассы": "НПС Черкассы",
    }
    bad = [(k, prep(k), v) for k, v in cases.items() if prep(k) != v]
    if bad:
        for k, got, want in bad:
            print("  ✗ %s -> %s (ждали %s)" % (k, got, want))
        return 1
    print("selfcheck OK: %d случаев — согласные, мягкий знак м/ж, прилагательные, "
          "мн.ч., дефисные, несклоняемые, многословные, приставки" % len(cases))
    return 0


if __name__ == "__main__":
    if "--selfcheck" in sys.argv:
        sys.exit(selfcheck())
    import json
    path = sys.argv[1] if len(sys.argv) > 1 else "data/strikes.json"
    d = json.load(open(path, encoding="utf-8"))
    cities = sorted({str(x.get("city", "")).strip() for x in d["strikes"] if x.get("city")})
    for c in cities:
        print("  в %-42s -> в %s" % (c, prep(c)))
    print("\nвсего:", len(cities))
