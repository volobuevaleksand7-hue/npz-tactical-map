def _entry_registry_line(key):
    p = AZS_PAGES[key]
    return json.dumps({
        "url": f"/{p['slug']}", "type": p["type"], "primary_kw": p["primary_kw"],
        "keywords": p["keywords"], "created": datetime.date.today().isoformat(),
        "status": "live",
        "note": "АЗС-ветка (воронка на карту наличия топлива); нейтральный тон. "
                "Сгенерено agents/azs-pages.py.",
    }, ensure_ascii=False)


def published():
    return {json.loads(l).get("url") for l in REG.read_text(encoding="utf-8").splitlines() if l.strip()}


def sh(cmd, **kw):
    print("$", " ".join(cmd)); return subprocess.run(cmd, cwd=ROOT, check=True, **kw)


def gen_root(key, today):
    _gf.PAGES = AZS_PAGES              # рендерим наш контент машинерией gen-fuel-pages
    html = _gf.render(key, today)
    (ROOT / f"{AZS_PAGES[key]['slug']}.html").write_text(html, encoding="utf-8")
    print("написано:", f"{AZS_PAGES[key]['slug']}.html")


def add_registry(key):
    slug_url = f"/{AZS_PAGES[key]['slug']}"
    if slug_url in published():
        print("реестр: уже есть", slug_url); return
    with REG.open("a", encoding="utf-8") as f:
        f.write(_entry_registry_line(key) + "\n")
    print("реестр: добавлено", slug_url)


def add_sitemap(key, today):
    loc = f"{SITE}/{AZS_PAGES[key]['slug']}"
    xml = SITEMAP.read_text(encoding="utf-8")
    if loc in xml:
        print("sitemap: уже есть"); return
    block = (f"  <url>\n    <loc>{loc}</loc>\n    <lastmod>{today}</lastmod>\n"
             f"    <changefreq>daily</changefreq>\n    <priority>0.8</priority>\n"
             f'    <xhtml:link rel="alternate" hreflang="ru" href="{loc}"/>\n  </url>\n')
    SITEMAP.write_text(xml.replace("</urlset>", block + "</urlset>", 1), encoding="utf-8")
    print("sitemap: добавлено", loc)


def main(argv):
    if not argv or argv[0] == "--list":
        for k, p in AZS_PAGES.items():
            print(f"{k:18} → /{p['slug']:18} [{p['type']}] {p['primary_kw']}")
        return
    key = argv[0]
    if key not in AZS_PAGES:
        sys.exit(f"нет страницы '{key}'. Доступны: {', '.join(AZS_PAGES)}")
    dry = "--dry-run" in argv
    today = datetime.date.today()
    gen_root(key, today)
    add_registry(key)
    add_sitemap(key, today.isoformat())
    sh([sys.executable, str(HERE / "build-nav.py")])
    sh([sys.executable, str(HERE / "check-ia.py")])
    if dry:
        print("--dry-run: коммит/пуш пропущены"); return
    sh(["git", "add", "-A"])
    slug_url = f"/{AZS_PAGES[key]['slug']}"
    msg = (f"seo: АЗС-страница {slug_url} ({AZS_PAGES[key]['primary_kw']})\n\n"
           f"Воронка на карту наличия топлива, нейтральный OSINT-тон. "
           f"Сгенерено agents/azs-pages.py.\n\nCo-Authored-By: Claude Opus 4.8 <noreply@anthropic.com>")
    subprocess.run(["git", "commit", "-m", msg], cwd=ROOT, check=True,
                   env={**os.environ, "ALLOW_FRONTEND_RELEASE": "1"})
    for _ in range(4):
        if subprocess.run(["git", "push", "origin", "main"], cwd=ROOT).returncode == 0:
            break
        subprocess.run(["git", "pull", "--rebase", "origin", "main"], cwd=ROOT, check=True)
    else:
        raise RuntimeError("push отклонён после 4 попыток")
    print(f"\n✅ опубликовано: {slug_url}")


if __name__ == "__main__":
    main(sys.argv[1:])
